void	ft_sort_int_tab(int *tab, int size)
{
	int i;
	int c;

	i = 0;
	size--;
	while (i < size)
	{
		if (tab[i + 1] < tab[i])
		{
			c = tab[i];
			tab[i] = tab[i + 1];
			tab[i + 1] = c;
			if (i > 0)
			{
				i--;
			}
		}
		else
			i++;
	}
}
